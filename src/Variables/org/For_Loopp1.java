package Variables.org;

public class For_Loopp1 {
	public static void main(String[] args) {
		String arr[] = { "for each loop" };
		int arr1[] = { 10, 12, 13, 14, 15 };
		long arr2[] = { 123456789012l};
		for (String i : arr)
			System.out.println(i);
		for (int a : arr1)
			System.out.println(a);
		for (long b : arr2)
			System.out.println(b);
	}
}
